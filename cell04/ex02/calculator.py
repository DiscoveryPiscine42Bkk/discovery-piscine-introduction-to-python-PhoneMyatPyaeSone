#!/usr/bin/env python3
first_num = int(input("Give me the first number: "))
second_num = int(input("Give me the second number: "))
print("Thank you!")

# addition
print(f"{first_num} + {second_num} = {first_num + second_num}")

# subtraction
print(f"{first_num} - {second_num} = {first_num - second_num}")

# division 
print(f"{first_num} / {second_num} = {first_num / second_num}")

# multiplication
print(f"{first_num} * {second_num} = {first_num * second_num}")